package DataTypes;

public class PrimitiveDatatypes {

    // 1. Boolean Data Type
    boolean isTrue = true;

    // 2. Byte Data Type (1 byte)
    byte age = 21;

    // 3. Char Data Type (2 bytes)
    char ch = 'P';

    // 4. Short Data Type (2 bytes)
    short stu = 1000;

    // 5. Int Data Types (4 bytes)
    int num = 1234;

    // 6. long Data Type (8 bytes)
    long l = 37436;

    // 7. Float Data type (4 bytes)
    float f = 32.5f;

    // 8. Double Data Type (8 bytes)
    double d = 23.325534;

    static void main() {
        System.out.println((char) 65430);
        System.out.println((int)Character.MAX_VALUE);
    }
}
