package DecisionMaking;

public class IfElseLadder {
    static void main() {

        int n = 20;

        if(n == 10) {
            System.out.println("n is 10");
        }
        else if(n == 15) {
            System.out.println("n is 15");
        }
        else if(n == 20) {
            System.out.println("n is 20");
        }
        else {
            System.out.println(n+" does not exist");
        }
    }
}
