package Operator;

public class Arithmetic {
    static void main() {

        int a = 12, b = 5;

        int add = a + b;
        int sub = a - b;
        int mul = a * b;
        int div = a / b;
        int mod = a % b;

        System.out.println("Addition: "+add);
        System.out.println("Subtraction: "+sub);
        System.out.println("Multiplication: "+mul);
        System.out.println("Division: "+div);
        System.out.println("Modulo: "+mod);
    }
}
