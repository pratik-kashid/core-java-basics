package Operator;

public class Assignment {

    static void main() {

        int a = 12;

        a += 5;
        System.out.println("Add 5: "+a);

        a -= 3;
        System.out.println("Sub 3: "+a);

        a *= 2;
        System.out.println("Mul 2: "+a);

        a /= 6;
        System.out.println("Div 6: "+a);

        a %= 5;
        System.out.println("Mod 5: "+a);

    }
}
